<?php

if(!function_exists('special_char_half_to_full')){
    function special_char_half_to_full($str)
    {
        $half = array(
            "!", "?", ",", ".", ":", ";",
            "+", "-", "=",
            "\\", "/",  "|",   "\"",  "'",  "`",   "(",   ")",  "{",  "}",  "[",   "]",  "<",  ">",
            "~", "@", "#", "$", "%", "^", "&","_","*",
        );
        $full = array(
            "！", "？", "，", "﹒", "：", "；",
            "＋", "－", "＝",
            "＼", "／", "｜", "＂", "’", "‵", "（", "）", "｛", "｝", "〔", "〕", "＜", "＞",
            "～", "＠", "＃", "＄", "％", "︿", "＆","＿","＊",

        );
        return str_replace($half, $full, $str);
    }
}
